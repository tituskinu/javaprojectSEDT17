package class7;

public class E2NestedLoops {
    //the easy way of doing nested loop
    public static void main(String[] args) {
        for (int i = 0; i < 2; i++) {
            for (int j = 0; j <10 ; j++) {
                System.out.print("+");

            }

            System.out.println("+");

        }

    }
}
