package class5;

public class ESwitch {
    public static void main(String[] args) {
        int day=4;
        switch (day){
            case 1:
                System.out.println("sunday");
                break;

            case 2:
                System.out.println("monday");
                break;
            case 3:
                System.out.println("Tuesday");
                break;
            default:
                System.out.println("Not a valid day number");





        }
    }
}
